package ar.edu.unahur.obj2.parte2.ejercicio1;

public class Persona {

    protected int edad;
    protected String nombre;


    public Persona(int edad, String nombre) {
        this.edad = edad;
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public String getNombre() {
        return nombre;
    }


}
